    function drawChar5x7(x: number, y: number, ch: number) {
        if (ch < 32 || ch > 126) ch = 63; // '?'
        const idx = (ch - 32) * 5;
        for (let col = 0; col < 5; col++) {
            const line = font5x7[idx + col];
            for (let row = 0; row < 7; row++) {
                const on = ((line >> row) & 0x01) !== 0;
                pixel(x + col, y + row, on);
            }
        }
        // 1px spacing
        for (let row = 0; row < 7; row++) pixel(x + 5, y + row, false);
    }

    /**
     * 文字列表示（5x7フォント） (x:0-127, y:0-63)
     */
    //% block="文字列 %text を x %x y %y に描く"
    //% x.min=0 x.max=127 y.min=0 y.max=63
    //% weight=70
    export function drawString(text: string, x: number, y: number): void {
        if (!inited) initInternal();
        let cx = x;
        for (let i = 0; i < text.length; i++) {
            drawChar5x7(cx, y, text.charCodeAt(i));
            cx += 6;
            if (cx + 5 >= WIDTH) break;
        }
    }

    /**
     * バッファを画面へ反映
     */
    //% block="表示更新"
    //% weight=60
    export function show(): void {
        if (!inited) initInternal();

        for (let page = 0; page < PAGES; page++) {
            writeCmd1(0xB0 + page); // page addr
            writeCmd1(0x00);        // lower col start
            writeCmd1(0x10);        // higher col start

            // send one page (128 bytes) with DATA control byte
            const out = pins.createBuffer(1 + WIDTH);
            out[0] = DATA;
            for (let i = 0; i < WIDTH; i++) {
                out[1 + i] = buf[page * WIDTH + i];
            }
            pins.i2cWriteBuffer(I2C_ADDR, out, false);
        }
    }
